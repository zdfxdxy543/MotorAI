"""
Schema definitions and validation helpers for the GMP parameter-iteration
agent evaluation pipeline.

This module intentionally contains no metric calculation logic. It only:
  1. Defines dataclass-based configuration models.
  2. Loads evaluation configuration from dictionaries or JSON files.
  3. Validates field types and references.

Python: 3.12+
Dependencies: standard library only
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Mapping
import json
import math


SUPPORTED_OPTIMIZATION_DIRECTIONS: set[str] = {
    "minimize",
    "maximize",
    "none",
}

# Keep this list broad enough for the first evaluation module, but still useful
# for catching accidental names such as "overshot" instead of "overshoot".
# Future modules may extend this set or pass a custom allowed_metric_names set
# into validate_evaluation_config().
SUPPORTED_METRIC_NAMES: set[str] = {
    "final_value",
    "peak_value",
    "min_value",
    "peak_to_peak",
    "mean_absolute_error",
    "rms_error",
    "steady_state_error",
    "overshoot",
    "rise_time",
    "settling_time",
    "ripple",
    "zero_crossing_count",
    "linear_fit_r2",
    "numerical_derivative",
    "response_delay",
    "stability",
    "energy_or_current_cost",
    "current_cost",
    # Common semantic aliases used by task-level evaluation configs.
    "final_speed_error",
    "final_position_error",
    "speed_overshoot",
    "iq_response_overshoot",
    "id_deviation",
    "oscillation_score",
    "acceleration_tracking_error",
    "velocity_curve_linearity",
    "current_smoothness",
}


class EvaluationConfigError(ValueError):
    """Raised when an evaluation configuration is malformed."""


@dataclass(slots=True)
class MetricConfig:
    """Configuration for one metric to be evaluated later.

    This class only stores what should be calculated. The actual calculation is
    implemented in later modules, such as metrics.py and evaluator.py.
    """

    metric_name: str
    signal: str
    target_signal: str | None = None
    target_value: Any | None = None
    weight: float = 1.0
    optimization_direction: str = "minimize"
    tolerance: float | None = None
    window: Any | None = None
    extra: dict[str, Any] = field(default_factory=dict)

    @classmethod
    def from_dict(cls, data: Mapping[str, Any]) -> "MetricConfig":
        """Create a MetricConfig from a dictionary.

        Unknown fields are preserved in extra so later modules can introduce
        fields such as good_threshold, bad_threshold, unit, description, etc.
        """
        if not isinstance(data, Mapping):
            raise EvaluationConfigError("Each metric must be a JSON object/dict.")

        known_fields = {
            "metric_name",
            "signal",
            "target_signal",
            "target_value",
            "weight",
            "optimization_direction",
            "tolerance",
            "window",
        }
        extra = {key: value for key, value in data.items() if key not in known_fields}

        return cls(
            metric_name=data.get("metric_name"),
            signal=data.get("signal"),
            target_signal=data.get("target_signal"),
            target_value=data.get("target_value"),
            weight=data.get("weight", 1.0),
            optimization_direction=data.get("optimization_direction", "minimize"),
            tolerance=data.get("tolerance"),
            window=data.get("window"),
            extra=extra,
        )

    def to_dict(self) -> dict[str, Any]:
        """Convert this metric configuration to a JSON-serializable dict."""
        data: dict[str, Any] = {
            "metric_name": self.metric_name,
            "signal": self.signal,
            "weight": self.weight,
            "optimization_direction": self.optimization_direction,
        }
        if self.target_signal is not None:
            data["target_signal"] = self.target_signal
        if self.target_value is not None:
            data["target_value"] = self.target_value
        if self.tolerance is not None:
            data["tolerance"] = self.tolerance
        if self.window is not None:
            data["window"] = self.window
        data.update(self.extra)
        return data


@dataclass(slots=True)
class EvaluationConfig:
    """Top-level evaluation configuration generated by the agent."""

    task_type: str
    objective: str
    signals: dict[str, str]
    metrics: list[MetricConfig]
    derived_signals: list[dict[str, Any]] = field(default_factory=list)
    extra: dict[str, Any] = field(default_factory=dict)

    @classmethod
    def from_dict(cls, data: Mapping[str, Any]) -> "EvaluationConfig":
        """Create an EvaluationConfig from a dictionary."""
        if not isinstance(data, Mapping):
            raise EvaluationConfigError("Evaluation config must be a JSON object/dict.")

        known_fields = {
            "task_type",
            "objective",
            "signals",
            "metrics",
            "derived_signals",
        }
        extra = {key: value for key, value in data.items() if key not in known_fields}

        raw_metrics = data.get("metrics")
        if raw_metrics is None:
            metrics: list[MetricConfig] = []
        elif isinstance(raw_metrics, list):
            metrics = [MetricConfig.from_dict(item) for item in raw_metrics]
        else:
            raise EvaluationConfigError("Field 'metrics' must be a list.")

        raw_signals = data.get("signals", {})
        signals = dict(raw_signals) if isinstance(raw_signals, Mapping) else raw_signals

        raw_derived_signals = data.get("derived_signals", [])
        derived_signals = list(raw_derived_signals) if isinstance(raw_derived_signals, list) else raw_derived_signals

        return cls(
            task_type=data.get("task_type"),
            objective=data.get("objective"),
            signals=signals,
            metrics=metrics,
            derived_signals=derived_signals,
            extra=extra,
        )

    @classmethod
    def from_json_file(cls, path: str | Path) -> "EvaluationConfig":
        """Load an EvaluationConfig from a JSON file."""
        json_path = Path(path)
        try:
            with json_path.open("r", encoding="utf-8") as f:
                data = json.load(f)
        except FileNotFoundError as exc:
            raise EvaluationConfigError(f"Evaluation config file not found: {json_path}") from exc
        except json.JSONDecodeError as exc:
            raise EvaluationConfigError(
                f"Evaluation config file is not valid JSON: {json_path}; {exc}"
            ) from exc

        return cls.from_dict(data)

    def to_dict(self) -> dict[str, Any]:
        """Convert this evaluation configuration to a JSON-serializable dict."""
        data: dict[str, Any] = {
            "task_type": self.task_type,
            "objective": self.objective,
            "signals": dict(self.signals),
            "metrics": [metric.to_dict() for metric in self.metrics],
        }
        if self.derived_signals:
            data["derived_signals"] = self.derived_signals
        data.update(self.extra)
        return data

    def to_json_file(self, path: str | Path) -> None:
        """Write this configuration to a JSON file."""
        json_path = Path(path)
        json_path.parent.mkdir(parents=True, exist_ok=True)
        with json_path.open("w", encoding="utf-8") as f:
            json.dump(self.to_dict(), f, ensure_ascii=False, indent=2)
            f.write("\n")


# Convenience aliases for callers that prefer functional style.
def load_evaluation_config_from_dict(data: Mapping[str, Any]) -> EvaluationConfig:
    """Load an EvaluationConfig from a dictionary without validating it."""
    return EvaluationConfig.from_dict(data)


def load_evaluation_config_from_json(path: str | Path) -> EvaluationConfig:
    """Load an EvaluationConfig from a JSON file without validating it."""
    return EvaluationConfig.from_json_file(path)


def validate_evaluation_config(
    config: EvaluationConfig,
    *,
    allowed_metric_names: set[str] | None = None,
    strict_metric_names: bool = True,
) -> EvaluationConfig:
    """Validate an EvaluationConfig.

    Args:
        config: Parsed EvaluationConfig object.
        allowed_metric_names: Optional custom metric-name allowlist. If omitted,
            SUPPORTED_METRIC_NAMES is used.
        strict_metric_names: If True, metric_name must be in the allowlist. If
            False, only non-empty string validation is applied.

    Returns:
        The same config object, enabling chained usage.

    Raises:
        EvaluationConfigError: If any field is invalid.
    """
    if not isinstance(config, EvaluationConfig):
        raise EvaluationConfigError("config must be an EvaluationConfig instance.")

    _validate_non_empty_string(config.task_type, "task_type")
    _validate_non_empty_string(config.objective, "objective")
    _validate_signals(config.signals)
    _validate_derived_signals(config.derived_signals)

    if not isinstance(config.metrics, list):
        raise EvaluationConfigError("Field 'metrics' must be a list.")
    if not config.metrics:
        raise EvaluationConfigError("Field 'metrics' must contain at least one metric.")

    logical_signal_names = set(config.signals.keys()) | _derived_signal_names(config.derived_signals)
    allowlist = allowed_metric_names if allowed_metric_names is not None else SUPPORTED_METRIC_NAMES

    for index, metric in enumerate(config.metrics):
        _validate_metric(
            metric=metric,
            index=index,
            logical_signal_names=logical_signal_names,
            allowed_metric_names=allowlist,
            strict_metric_names=strict_metric_names,
        )

    return config


def _validate_metric(
    *,
    metric: MetricConfig,
    index: int,
    logical_signal_names: set[str],
    allowed_metric_names: set[str],
    strict_metric_names: bool,
) -> None:
    if not isinstance(metric, MetricConfig):
        raise EvaluationConfigError(f"metrics[{index}] must be a MetricConfig instance.")

    prefix = f"metrics[{index}]"
    _validate_non_empty_string(metric.metric_name, f"{prefix}.metric_name")
    _validate_non_empty_string(metric.signal, f"{prefix}.signal")

    if strict_metric_names and metric.metric_name not in allowed_metric_names:
        supported = ", ".join(sorted(allowed_metric_names))
        raise EvaluationConfigError(
            f"{prefix}.metric_name '{metric.metric_name}' is not supported. "
            f"Supported metric names: {supported}"
        )

    if metric.signal not in logical_signal_names:
        raise EvaluationConfigError(
            f"{prefix}.signal '{metric.signal}' is not defined in signals or derived_signals."
        )

    if metric.target_signal is not None:
        _validate_non_empty_string(metric.target_signal, f"{prefix}.target_signal")
        if metric.target_signal not in logical_signal_names:
            raise EvaluationConfigError(
                f"{prefix}.target_signal '{metric.target_signal}' is not defined in signals or derived_signals."
            )

    if metric.target_signal is not None and metric.target_value is not None:
        raise EvaluationConfigError(
            f"{prefix} must not define both target_signal and target_value. Choose one."
        )

    metric.weight = _coerce_finite_float(metric.weight, f"{prefix}.weight")
    if metric.weight < 0:
        raise EvaluationConfigError(f"{prefix}.weight must be >= 0.")

    _validate_non_empty_string(metric.optimization_direction, f"{prefix}.optimization_direction")
    if metric.optimization_direction not in SUPPORTED_OPTIMIZATION_DIRECTIONS:
        allowed = ", ".join(sorted(SUPPORTED_OPTIMIZATION_DIRECTIONS))
        raise EvaluationConfigError(
            f"{prefix}.optimization_direction must be one of: {allowed}."
        )

    if metric.tolerance is not None:
        metric.tolerance = _coerce_finite_float(metric.tolerance, f"{prefix}.tolerance")
        if metric.tolerance < 0:
            raise EvaluationConfigError(f"{prefix}.tolerance must be >= 0.")


def _validate_signals(signals: Any) -> None:
    if not isinstance(signals, dict):
        raise EvaluationConfigError("Field 'signals' must be a dict mapping logical names to source names.")
    if not signals:
        raise EvaluationConfigError("Field 'signals' must contain at least one signal.")

    for logical_name, source_name in signals.items():
        _validate_non_empty_string(logical_name, "signals key")
        _validate_non_empty_string(source_name, f"signals['{logical_name}']")


def _validate_derived_signals(derived_signals: Any) -> None:
    if not isinstance(derived_signals, list):
        raise EvaluationConfigError("Field 'derived_signals' must be a list when provided.")

    seen_names: set[str] = set()
    for index, item in enumerate(derived_signals):
        if not isinstance(item, dict):
            raise EvaluationConfigError(f"derived_signals[{index}] must be a dict.")
        name = item.get("name")
        _validate_non_empty_string(name, f"derived_signals[{index}].name")
        if name in seen_names:
            raise EvaluationConfigError(f"Duplicate derived signal name: '{name}'.")
        seen_names.add(name)


def _derived_signal_names(derived_signals: list[dict[str, Any]]) -> set[str]:
    return {item["name"] for item in derived_signals if isinstance(item, dict) and "name" in item}


def _validate_non_empty_string(value: Any, field_name: str) -> None:
    if not isinstance(value, str) or not value.strip():
        raise EvaluationConfigError(f"Field '{field_name}' must be a non-empty string.")


def _coerce_finite_float(value: Any, field_name: str) -> float:
    if isinstance(value, bool):
        raise EvaluationConfigError(f"Field '{field_name}' must be a number, not bool.")
    try:
        number = float(value)
    except (TypeError, ValueError) as exc:
        raise EvaluationConfigError(f"Field '{field_name}' must be a finite number.") from exc
    if not math.isfinite(number):
        raise EvaluationConfigError(f"Field '{field_name}' must be a finite number.")
    return number


def _main() -> int:
    """Small command-line validator for manual debugging.

    Example:
        python -m agent_core.evaluation.schemas ../log/evaluation_config.json
    """
    import argparse

    parser = argparse.ArgumentParser(description="Validate an evaluation_config.json file.")
    parser.add_argument("config_path", help="Path to evaluation_config.json")
    parser.add_argument(
        "--allow-unknown-metrics",
        action="store_true",
        help="Only check that metric_name is a non-empty string.",
    )
    args = parser.parse_args()

    config = load_evaluation_config_from_json(args.config_path)
    validate_evaluation_config(config, strict_metric_names=not args.allow_unknown_metrics)
    print(f"OK: evaluation config is valid: {args.config_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(_main())
