"""
evaluation.py

Agent tool adapters for the GMP parameter-iteration evaluation layer.

This module exposes three LLM-callable tools:
1. write_evaluation_config
2. evaluate_simulation_result
3. read_evaluation_result

It intentionally contains only tool orchestration code. Deterministic parsing,
metric computation, scoring, and result writing stay in agent_core/evaluation/.

Python version: 3.12+
Dependencies: standard library only
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, Mapping, Optional
import json

from ..config import ProjectContext
from ..constants import DEFAULT_MAX_CHARS
from ..evaluation.evaluator import evaluate_simulation
from ..evaluation.result_writer import write_json_result, write_text_summary
from ..evaluation.schemas import (
    EvaluationConfigError,
    load_evaluation_config_from_dict,
    validate_evaluation_config,
)
from ..tool_registry import ToolRegistry
from ..utils import read_text_if_exists, truncate_text


class EvaluationTools:
    """Tool-call wrapper around the deterministic evaluation modules."""

    def __init__(self, ctx: ProjectContext) -> None:
        self.ctx = ctx

    def _automation(self) -> Dict[str, Any]:
        return self.ctx.automation()

    def _path_from_automation(self, key: str, default: str) -> Path:
        """Resolve an automation path relative to agent_project.json."""
        raw_path = self._automation().get(key, default)
        return self.ctx.resolve_config_path(str(raw_path))

    def _evaluation_config_path(self) -> Path:
        return self._path_from_automation("evaluation_config", "../log/evaluation_config.json")

    def _evaluation_result_path(self) -> Path:
        return self._path_from_automation("evaluation_result", "../log/evaluation_result.json")

    def _evaluation_summary_path(self) -> Path:
        return self._path_from_automation("evaluation_summary", "../log/evaluation_summary.txt")

    def _processed_json_path(self) -> Path:
        # Reuse the existing automation key used by run_simulation_bat().
        return self._path_from_automation("simulation_result", "../log/processed.json")

    def write_evaluation_config(
        self,
        evaluation_config: Mapping[str, Any],
        *,
        allow_unknown_metrics: bool = False,
    ) -> str:
        """
        Validate and write ../log/evaluation_config.json.

        Args:
            evaluation_config:
                Dict generated by the agent from the user objective.

            allow_unknown_metrics:
                If True, schemas.py only checks that metric_name is a non-empty
                string. Normal agent use should keep this False.
        """
        if not isinstance(evaluation_config, Mapping):
            return (
                "Error: write_evaluation_config expects argument 'evaluation_config' "
                f"to be an object/dict, got {type(evaluation_config).__name__}."
            )

        output_path = self._evaluation_config_path()

        try:
            config = load_evaluation_config_from_dict(evaluation_config)
            validate_evaluation_config(
                config,
                strict_metric_names=not allow_unknown_metrics,
            )
            config.to_json_file(output_path)
        except (EvaluationConfigError, OSError, TypeError, ValueError) as exc:
            return f"Error: failed to write evaluation_config.json. {type(exc).__name__}: {exc}"

        return (
            "OK: evaluation_config.json written.\n"
            f"Path: {output_path}\n"
            f"Task Type: {config.task_type}\n"
            f"Objective: {config.objective}\n"
            f"Signal Count: {len(config.signals)}\n"
            f"Derived Signal Count: {len(config.derived_signals)}\n"
            f"Metric Count: {len(config.metrics)}"
        )

    def evaluate_simulation_result(
        self,
        *,
        evaluation_config_path: Optional[str] = None,
        allow_unknown_metrics: bool = False,
        include_traceback: bool = False,
        summary_max_chars: int = 4000,
    ) -> str:
        """
        Evaluate processed.json with an evaluation config JSON and write outputs.

        Args:
            evaluation_config_path:
                Optional explicit path to the evaluation config JSON. When omitted,
                the path configured by automation.evaluation_config is used.

        Outputs:
            ../log/evaluation_result.json
            ../log/evaluation_summary.txt
        """
        processed_json_path = self._processed_json_path()
        if evaluation_config_path:
            evaluation_config_path_obj = Path(evaluation_config_path).expanduser().resolve()
        else:
            evaluation_config_path_obj = self._evaluation_config_path()
        evaluation_config_path = evaluation_config_path_obj
        evaluation_result_path = self._evaluation_result_path()
        evaluation_summary_path = self._evaluation_summary_path()

        if not processed_json_path.exists():
            return f"Error: processed.json does not exist: {processed_json_path}"
        if not processed_json_path.is_file():
            return f"Error: processed_json_path is not a file: {processed_json_path}"
        if not evaluation_config_path.exists():
            return f"Error: evaluation_config.json does not exist: {evaluation_config_path}"
        if not evaluation_config_path.is_file():
            return f"Error: evaluation_config_path is not a file: {evaluation_config_path}"

        try:
            result = evaluate_simulation(
                processed_json_path=processed_json_path,
                evaluation_config_path=evaluation_config_path,
                strict_metric_names=not allow_unknown_metrics,
                include_traceback=include_traceback,
            )
            write_json_result(result, evaluation_result_path)
            write_text_summary(result, evaluation_summary_path)
        except Exception as exc:  # Tool boundary: convert all internal failures to a string.
            return f"Error: failed to evaluate simulation result. {type(exc).__name__}: {exc}"

        status = result.get("status", "unknown")
        overall_score = result.get("overall_score", None)
        metric_ok_count = result.get("metric_ok_count", 0)
        metric_error_count = result.get("metric_error_count", 0)
        warning_count = len(result.get("warnings", []) or [])
        error_count = len(result.get("errors", []) or [])

        summary_text = read_text_if_exists(evaluation_summary_path, summary_max_chars)

        return (
            "OK: evaluation finished.\n"
            f"Status: {status}\n"
            f"Overall Score: {overall_score}\n"
            f"Metric OK Count: {metric_ok_count}\n"
            f"Metric Error Count: {metric_error_count}\n"
            f"Warning Count: {warning_count}\n"
            f"Error Count: {error_count}\n"
            f"Processed JSON: {processed_json_path}\n"
            f"Evaluation Config: {evaluation_config_path}\n"
            f"Evaluation Result: {evaluation_result_path}\n"
            f"Evaluation Summary: {evaluation_summary_path}\n\n"
            f"[evaluation_summary.txt]\n{summary_text}"
        )

    def read_evaluation_result(self, max_chars: int = DEFAULT_MAX_CHARS) -> str:
        """Read the latest ../log/evaluation_result.json."""
        result_path = self._evaluation_result_path()

        if not result_path.exists():
            return f"Error: evaluation_result.json does not exist: {result_path}"
        if not result_path.is_file():
            return f"Error: evaluation_result path is not a file: {result_path}"

        content = read_text_if_exists(result_path, max_chars)

        # Add a compact header when the result JSON can be parsed. This keeps
        # agent-facing output easy to scan without hiding the raw JSON.
        header = ""
        try:
            data = json.loads(result_path.read_text(encoding="utf-8-sig"))
            if isinstance(data, dict):
                header = (
                    "Evaluation Result Summary\n"
                    f"Status: {data.get('status')}\n"
                    f"Task Type: {data.get('task_type')}\n"
                    f"Overall Score: {data.get('overall_score')}\n"
                    f"Metric OK Count: {data.get('metric_ok_count')}\n"
                    f"Metric Error Count: {data.get('metric_error_count')}\n\n"
                )
        except (OSError, json.JSONDecodeError):
            header = ""

        return f"[FILE: {result_path}]\n\n{header}{truncate_text(content, max_chars)}"


def register_evaluation_tools(registry: ToolRegistry, ctx: ProjectContext) -> EvaluationTools:
    tool = EvaluationTools(ctx)

    registry.register(
        "write_evaluation_config",
        {
            "type": "function",
            "function": {
                "name": "write_evaluation_config",
                "description": (
                    "Validate and write ../log/evaluation_config.json. Use this after "
                    "planning which signals and metrics should evaluate the user's motor-control objective."
                ),
                "parameters": {
                    "type": "object",
                    "properties": {
                        "evaluation_config": {
                            "type": "object",
                            "description": (
                                "Evaluation config object containing task_type, objective, signals, "
                                "optional derived_signals, and metrics."
                            ),
                        },
                        "allow_unknown_metrics": {
                            "type": "boolean",
                            "description": (
                                "Optional. Default false. If true, only basic metric_name validation is applied."
                            ),
                        },
                    },
                    "required": ["evaluation_config"],
                },
            },
        },
        lambda args: tool.write_evaluation_config(
            evaluation_config=args["evaluation_config"],
            allow_unknown_metrics=bool(args.get("allow_unknown_metrics", False)),
        ),
    )

    registry.register(
        "evaluate_simulation_result",
        {
            "type": "function",
            "function": {
                "name": "evaluate_simulation_result",
                "description": (
                    "Run deterministic evaluation using the configured processed.json and evaluation config JSON. "
                    "If evaluation_config_path is provided, use it; otherwise use automation.evaluation_config. "
                    "Then write evaluation_result.json and evaluation_summary.txt."
                ),
                "parameters": {
                    "type": "object",
                    "properties": {
                        "evaluation_config_path": {
                            "type": "string",
                            "description": (
                                "Optional. Explicit path to the evaluation config JSON. "
                                "Use the job/Main JSON path when it already contains task_type, objective, signals, and metrics."
                            ),
                        },
                        "allow_unknown_metrics": {
                            "type": "boolean",
                            "description": (
                                "Optional. Default false. If true, config validation allows unknown metric names, "
                                "but evaluator dispatch may still mark them as errors."
                            ),
                        },
                        "include_traceback": {
                            "type": "boolean",
                            "description": "Optional. Default false. Include Python tracebacks in evaluation errors.",
                        },
                        "summary_max_chars": {
                            "type": "integer",
                            "description": "Optional. Maximum characters of evaluation_summary.txt returned to the agent.",
                        },
                    },
                },
            },
        },
        lambda args: tool.evaluate_simulation_result(
            evaluation_config_path=args.get("evaluation_config_path"),
            allow_unknown_metrics=bool(args.get("allow_unknown_metrics", False)),
            include_traceback=bool(args.get("include_traceback", False)),
            summary_max_chars=int(args.get("summary_max_chars", 4000)),
        ),
    )

    registry.register(
        "read_evaluation_result",
        {
            "type": "function",
            "function": {
                "name": "read_evaluation_result",
                "description": "Read the latest ../log/evaluation_result.json.",
                "parameters": {
                    "type": "object",
                    "properties": {
                        "max_chars": {
                            "type": "integer",
                            "description": "Maximum characters to return. Default is 20000.",
                        }
                    },
                },
            },
        },
        lambda args: tool.read_evaluation_result(
            max_chars=int(args.get("max_chars", DEFAULT_MAX_CHARS)),
        ),
    )

    return tool


__all__ = ["EvaluationTools", "register_evaluation_tools"]
